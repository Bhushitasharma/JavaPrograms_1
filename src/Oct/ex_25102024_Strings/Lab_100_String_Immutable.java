package Oct.ex_25102024_Strings;

public class Lab_100_String_Immutable {
    public static void main(String[] args) {
        String name = "bhushita";
        name.toUpperCase();
        System.out.println(name);
        // converts all the lower case into upper case"
        //bhushita -> BHUSHITA
        name = name.toUpperCase();
        System.out.println(name);
    }


}
