public class Task1_reverse_number {
    public static void main(String[] args) {
        int i = 5;
        for (i = 1; i <= 5; i++) {
            System.out.println("*");

        for (int j = 4; j >= 1; j--) {
            System.out.print("@");
        }


    }

}}
