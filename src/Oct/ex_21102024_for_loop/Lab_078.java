package Oct.ex_21102024_for_loop;

public class Lab_078 {
    public static void main(String[] args) {
        //To fine even number from 1 to 50
        for (int i =0 ; i<50 ; i++){
            if (i%2==0){
                System.out.println("even" +i);
            } else
            {
                System.out.println("odd");
            }
        }
    }
}
